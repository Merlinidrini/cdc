
public class Album
{
	public int id;
	public String name;
	public String artist;
	public int genre;
	public String genre_name;
	public int year;
	
	public int getId()
	{
		return id;
	}
	
	public String getName()
	{
		return name;
	}
	
	public String getArtist()
	{
		return artist;
	}
	
	public int getGenre()
	{
		return genre;
	}
	
	public String getGenreName()
	{
		return genre_name;
	}
	
	public int getYear()
	{
		return year;
	}
}
